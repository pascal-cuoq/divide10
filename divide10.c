uint64_t divide(uint64_t x, uint64_t n) {
  if (n == 0) return x;
  if (n >= 20) return 0;

  uint64_t factor = div_factor[n-1];
  x >>= n;
  __uint128_t product = factor * (__uint128_t)x;
  uint64_t result = product >> 64;
  result >>= 2*n;
  return result;
}
